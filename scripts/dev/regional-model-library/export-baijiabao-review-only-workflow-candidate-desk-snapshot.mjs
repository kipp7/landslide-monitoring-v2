import path from "node:path";
import { mkdir, readFile, writeFile } from "node:fs/promises";

const DEFAULT_CANDIDATE_JSON =
  ".tmp/regional-model-library/out/artifacts/baijiabao-review-only-workflow-candidate/baijiabao-review-only-workflow-candidate.json";
const DEFAULT_OUT_TS = "apps/desk/src/data/baijiabaoReviewQueueSnapshot.ts";

function parseArgs(argv) {
  const parsed = {
    candidateJson: DEFAULT_CANDIDATE_JSON,
    outTs: DEFAULT_OUT_TS
  };
  for (let index = 0; index < argv.length; index += 1) {
    const token = argv[index];
    if (token === "--candidate-json") parsed.candidateJson = argv[++index] ?? parsed.candidateJson;
    if (token === "--out-ts") parsed.outTs = argv[++index] ?? parsed.outTs;
  }
  return parsed;
}

function toDeskSnapshot(candidate) {
  return {
    generatedAt: candidate.generatedAt,
    artifactKey: candidate.artifactKey,
    status: candidate.status,
    sourceSummary: candidate.sourceSummary,
    productGate: candidate.productGate,
    runtimeBoundary: candidate.runtimeBoundary,
    summary: candidate.summary,
    items: candidate.items.map((item) => ({
      queueItemId: item.queueItemId,
      sourceReviewItemId: item.sourceReviewItemId,
      pointId: item.pointId,
      priority: item.priority,
      severity: item.severity,
      recommendedAction: item.recommendedAction,
      status: item.status,
      window: item.window,
      evidenceSummary: {
        utilityClass: item.evidenceSummary.utilityClass,
        classificationMix: item.evidenceSummary.classificationMix,
        evidenceRowCount: item.evidenceSummary.evidenceRowCount,
        immediatePositiveDays: item.evidenceSummary.immediatePositiveDays,
        greyZoneDays: item.evidenceSummary.greyZoneDays,
        within30Days: item.evidenceSummary.within30Days,
        isolatedDays: item.evidenceSummary.isolatedDays,
        maxBoosterScore: item.evidenceSummary.maxBoosterScore
      },
      autoReview: {
        finalClass: item.autoReview.finalClass,
        useful: item.autoReview.useful,
        confidence: item.autoReview.confidence,
        rule: item.autoReview.rule,
        rawEvidenceNeeded: item.autoReview.rawEvidenceNeeded,
        warning: item.autoReview.warning
      }
    }))
  };
}

async function main() {
  const repoRoot = process.cwd();
  const args = parseArgs(process.argv.slice(2));
  const candidateJson = path.resolve(repoRoot, args.candidateJson);
  const outTs = path.resolve(repoRoot, args.outTs);
  const candidate = JSON.parse(await readFile(candidateJson, "utf-8"));
  const snapshot = toDeskSnapshot(candidate);
  const content = `// Generated by scripts/dev/regional-model-library/export-baijiabao-review-only-workflow-candidate-desk-snapshot.mjs\n// Source artifact: ${args.candidateJson}\n\nexport const baijiabaoReviewQueueSnapshot = ${JSON.stringify(snapshot, null, 2)} as const;\n\nexport type BaijiabaoReviewQueueSnapshot = typeof baijiabaoReviewQueueSnapshot;\nexport type BaijiabaoReviewQueueItem = BaijiabaoReviewQueueSnapshot[\"items\"][number];\n`;
  await mkdir(path.dirname(outTs), { recursive: true });
  await writeFile(outTs, content, "utf-8");
  console.log(JSON.stringify({ outTs, itemCount: snapshot.items.length, artifactKey: snapshot.artifactKey }, null, 2));
}

main().catch((error) => {
  console.error(error instanceof Error ? error.stack ?? error.message : String(error));
  process.exitCode = 1;
});
